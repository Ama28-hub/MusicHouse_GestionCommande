    private void chargerUtilisateurs() {
        try {
            //System.out.println("üìå Chargement des utilisateurs..."); // Debug

            List<Users> listeUsers = usersCRUD.getAllUsersWithEmployes();
            tableModel = (DefaultTableModel) jTableUsers.getModel(); // Initialisation du mod√®le
            tableModel.setRowCount(0);

            if (listeUsers.isEmpty()) {
                System.out.println("‚ùå Aucun utilisateur trouv√© !");
            } else {
                for (Users user : listeUsers) {
                    //System.out.println("üìå Utilisateur r√©cup√©r√© : " + user.getNom() + " - " + user.getEmail()); // Debug
                    tableModel.addRow(new Object[]{
                        user.getIdUser(),
                        user.getNom(),
                        user.getPrenom(),
                        user.getEmail(),
                        user.getRole()
                    });
                }
            }

            // Ajout du tri et filtrage
            rowSorter = new TableRowSorter<>(tableModel);
            jTableUsers.setRowSorter(rowSorter);

            jTableUsers.revalidate();
            jTableUsers.repaint();

        } catch (Exception e) {
            System.out.println("‚ùå Erreur de chargement : " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur de chargement : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }


    
    private void rechercherUtilisateur() {
        String searchText = jTextFieldsearch.getText().trim();
        if (searchText.isEmpty()) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText));
        }
    }
    
    /**
     * Ajoute un nouvel utilisateur
     */
    private void ajouterUtilisateur() {
        new FormulaireUtilisateur(this, true, null).setVisible(true);
        chargerUtilisateurs();
    }

    /**
     * Modifie un utilisateur s√©lectionn√©
     */
    private void modifierUtilisateur() {
        int selectedRow = jTableUsers.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un utilisateur √† modifier.");
            return;
        }

        try {
            int id = Integer.parseInt(jTableUsers.getValueAt(selectedRow, 0).toString());
            Users user = usersCRUD.getUserById(id);

            if (user != null) {
                FormulaireUtilisateur form = new FormulaireUtilisateur(this, true, user);
                form.setVisible(true);
                chargerUtilisateurs();
            } else {
                JOptionPane.showMessageDialog(this, "Utilisateur introuvable.", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Anonymise un utilisateur au lieu de le supprimer
     */
    private void supprimerUtilisateur() {
        int selectedRow = jTableUsers.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un utilisateur √† supprimer.");
            return;
        }

        try {
            int id = Integer.parseInt(jTableUsers.getValueAt(selectedRow, 0).toString());
            int confirm = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment anonymiser cet utilisateur ?", "Confirmation", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                boolean success = usersCRUD.anonymiserUtilisateur(id);

                if (success) {
                    JOptionPane.showMessageDialog(this, "Utilisateur anonymis√© avec succ√®s !");
                    chargerUtilisateurs();
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de l'anonymisation.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

