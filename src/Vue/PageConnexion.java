    private boolean verifierConnexionBD() {
        try {
            Connection conn = Data.getConnection();
            if (conn != null) {
                conn.close();
                return true; // Connexion r√©ussie
            }
        } catch (Exception e) {
            return false; // √âchec de connexion
        }
        return false;
    }

    private void verifierConnexion() {

        if (!verifierConnexionBD()) {
            JOptionPane.showMessageDialog(this, "Impossible de se connecter √† la base de donn√©es.", "Erreur", JOptionPane.ERROR_MESSAGE);
            demanderConnexionLocale();
            return;
        }

        String email = jTxtUsername.getText().trim();
        String motDePasseSaisi = new String(jPasswordFieldMDP.getPassword()).trim();

        if (email.isEmpty() || motDePasseSaisi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un email et un mot de passe.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            UsersCRUD usersCRUD = new UsersCRUD();
            Users user = usersCRUD.getUserByEmail(email);

            if (user != null) {
                String storedPassword = user.getMotDePasse();
                //System.out.println("üìå Mot de passe r√©cup√©r√© depuis la base pour " + email + " : " + storedPassword);

                String decryptedPassword = storedPassword; // On suppose qu'il est en clair par d√©faut

                // V√©rifier si le mot de passe est chiffr√© avec AES
                if (storedPassword.startsWith("ENC(") && storedPassword.endsWith(")")) {
                    try {
                        String encryptedPart = storedPassword.substring(4, storedPassword.length() - 1);
                        decryptedPassword = Chiffrement.dechiffrer(encryptedPart);

                        if (decryptedPassword != null) {
                            decryptedPassword = decryptedPassword.trim();
                        }

                        //System.out.println("üîì Mot de passe d√©chiffr√© pour " + email + " : " + decryptedPassword);
                    } catch (Exception e) {
                        //System.err.println("‚ùå Erreur lors du d√©chiffrement du mot de passe : " + e.getMessage());
                        JOptionPane.showMessageDialog(this, "Erreur lors du d√©chiffrement du mot de passe.", "Erreur", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }

                // Comparaison stricte du mot de passe saisi avec celui d√©chiffr√©
                if (motDePasseSaisi.equals(decryptedPassword)) {
                    JOptionPane.showMessageDialog(this, "Connexion r√©ussie, bienvenue " + user.getNom() + " !");
                    Users.startSession(user);

                    if ("admin".equalsIgnoreCase(user.getRole())) {
                        new PageAdmin(this).setVisible(true);
                    } else {
                        new PageEmploye(this).setVisible(true);
                    }
                    this.setVisible(false);
                } else {
                    //System.err.println("‚ùå Mot de passe incorrect : Saisi [" + motDePasseSaisi + "] vs D√©chiffr√© [" + decryptedPassword + "]");
                    JOptionPane.showMessageDialog(this, "Mot de passe incorrect.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aucun utilisateur trouv√© avec cet email.", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur de connexion : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }


    private void demanderConnexionLocale() {
    // R√©cup√©ration des valeurs des champs de connexion existants
        String username = jTxtUsername.getText().trim();
        String password = new String(jPasswordFieldMDP.getPassword()).trim();

        // V√©rification des informations de connexion
        if (DEFAULT_USERNAME.equals(username) && DEFAULT_PASSWORD.equals(password)) {
            JOptionPane.showMessageDialog(this, "Connexion r√©ussie aux param√®tres.");
            new PageConfig(this, true).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Identifiants incorrects.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }


    
